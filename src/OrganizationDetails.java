
public class OrganizationDetails {
   
	private String orgid;
	private String orgname;
	private String address;
	private String description;
	private String establishmentDate;
	
	public String getOrgname() {
		return orgname;
	}
	public String setOrgname(String orgname) {
		return this.orgname=orgname;
	}
	public String getAddress() {
		return address;
	}
	public String setAddress(String address) {
		return this.address = address;
	}
	public String getDescription() {
		return description;
	}
	public String setDescription(String description) {
		return this.description = description;
	}
	public String getEstablishmentDate() {
		return establishmentDate;
	}
	public String setEstablishmentDate(String establishmentDate) {
		return this.establishmentDate = establishmentDate;
	}
	public String getOrgId() {
		return orgid;
	}
	public String setOrgId(String orgid) {
		return this.orgid=orgid;
	}
}
